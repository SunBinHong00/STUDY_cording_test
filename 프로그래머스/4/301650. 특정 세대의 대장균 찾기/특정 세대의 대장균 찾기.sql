WITH FIRST AS (
    SELECT ID
    FROM ECOLI_DATA
    WHERE PARENT_ID IS NULL
    ),
    
SECOND AS (
    SELECT B.ID
    FROM FIRST A
    JOIN ECOLI_DATA B ON A.ID=B.PARENT_ID
),

THIRD AS (
    SELECT C.ID
    FROM SECOND B
    JOIN ECOLI_DATA C ON B.ID=C.PARENT_ID
)



    
SELECT ID
FROM THIRD
ORDER BY ID